var default_language='ch'

export class TextFactory{ //用于获取文本
    constructor(dictionary,language){ //参数language不是必须的
        this.dictionary=dictionary
        this.language=language
        if(this.language==undefined){
            this.language=default_language //默认语言为中文
        }
    }
    str(key){
        if(!!this.dictionary[key])
            return this.dictionary[key][this.language]
        else{
            //console.log('no dict item:\''+key+'\'')
            return 'err:no dict item'
        }
            
    }
    note(key){
        if(!!this.dictionary[key])
            return this.dictionary[key]['note_'+this.language]
        else{
            //console.log('no dict item:\''+key+'\'')
            return 'err:no dict item'
        }
            
    }
}

